num = 10
print("Initial num:", num)

# Using augmented assignment operators
num += 5  # Equivalent to num = num + 5
print("After += 5:", num)

num -= 3  # Equivalent to num = num - 3
print("After -= 3:", num)

num *= 2  # Equivalent to num = num * 2
print("After *= 2:", num)

num /= 4  # Equivalent to num = num / 4
print("After /= 4:", num)

num **= 2  # Equivalent to num = num ** 2
print("After **= 2:", num)

num %= 3  # Equivalent to num = num % 3
print("After %= 3:", num)

# Convert integer to float
int_num = 10
float_num = float(int_num)
print(f"Integer {int_num} converted to float: {float_num}")

# Convert float to integer
float_num = 10.8
int_num = int(float_num)
print(f"Float {float_num} converted to integer: {int_num}")

# Convert string to integer
string_num = "25"
int_num = int(string_num)
print(f"String '{string_num}' converted to integer: {int_num}")

# Convert integer to string
int_num = 15
string_num = str(int_num)
print(f"Integer {int_num} converted to string: '{string_num}'")

# Convert boolean to integer
bool_val = True
int_bool = int(bool_val)
print(f"Boolean {bool_val} converted to integer: {int_bool}")

# Integer
int_num = 10
print(f"Integer: {int_num}, Type: {type(int_num)}")

# Float
float_num = 10.5
print(f"Float: {float_num}, Type: {type(float_num)}")

# String
string_text = "Hello, Python!"
print(f"String: '{string_text}', Type: {type(string_text)}")

# Boolean
bool_val = True
print(f"Boolean: {bool_val}, Type: {type(bool_val)}")

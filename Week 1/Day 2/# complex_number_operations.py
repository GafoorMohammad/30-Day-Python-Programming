a = complex(2, 3)  # Create a complex number 2 + 3j
b = complex(1, -4)  # Create a complex number 1 - 4j

print(f"a = {a}")
print(f"b = {b}")

# Addition
print(f"a + b = {a + b}")

# Subtraction
print(f"a - b = {a - b}")

# Multiplication
print(f"a * b = {a * b}")

# Division
print(f"a / b = {a / b}")

# Example with break
print("Example with break:")
for i in range(1, 11):
    if i == 5:
        break  # Exit the loop when i is 5
    print(i)

# Example with continue
print("\nExample with continue:")
for i in range(1, 11):
    if i == 5:
        continue  # Skip the iteration when i is 5
    print(i)
